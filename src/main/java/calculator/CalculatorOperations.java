package calculator;

public class CalculatorOperations {
	public int addOperation(int a,int b) {
		return(a+b);
	}
	
	public int subOperation(int a,int b) {
		return(a-b);
	}
	
	public int multiOperation(int a,int b) {
		return(a*b);
	}
	
	public double divOperation(int a,int b) {
		return(a/(double)b);
	}
}
